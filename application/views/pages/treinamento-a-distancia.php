			<p class="red"><strong><span>Programa Modular para Treinamento a Distância</strong></span></p>

			<p>O Programa Modular para Treinamento a Distância em “Gestão Estratégica com o Uso do Balanced Scorecard” propõe-se a capacitar os participantes nos conceitos que envolvem o desenvolvimento de um modelo de gestão estratégica utilizando o BSC. O programa é dividido em 9 módulos/fascículos que irão proporcionar um conhecimento detalhado sobre o tema. Nesse formato o participante decide o local e horário mais apropriado para estudar e, durante um período estipulado, poderá enviar questões e dúvidas para serem respondidas pelos instrutores.</p>

			<p>Os módulos estão totalmente alinhados aos conceitos originais desenvolvidos por Robert Kaplan e David Norton além de incorporarem todos os avanços na metodologia e sua utilização no mercado brasileiro em empresas com e sem fins lucrativos e empresas do governo.</p>

			<p>O curso está orientado a atender profissionais que participem ou irão participar de projetos de construção do Balanced Scorecard e àqueles que buscam um melhor entendimento da abordagem para utilização no dia-a-dia da gestão estratégica. </p>

