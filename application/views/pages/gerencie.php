			<p class="red"><strong>MODELO DE GESTÃO ESTRATÉGICA</strong></p>

			<p>O contexto competitivo torna o ambiente de negócios constantemente mutável. Isso exige das organizações uma grande capacidade de adaptação. A estratégia sendo uma hipótese formulada por processos de planejamento deve, neste contexto, ser gerenciada constantemente a fim de que as mudanças no ambiente sejam incorporadas.Um modelo de gestão estratégica deve garantir que a performance da estratégia seja analisada freqüentemente e que decisões sejam tomadas para correção de rumo. O desenho deste modelo passa pela definição do processo, do fluxo de informações, papéis e responsabilidades, meios e momentos da gestão estratégica. Além disso, deve ser aderente ao contexto e às necessidades da organização. A 3GEN possui grande experiência na estruturação de modelos efetivos de gestão estratégica do negócio.</p>

			
			<p class="red"><strong>REUNIÃO GEN</strong></p>

			<p>A Reunião de Gestão Estratégica do Negócio (GEN) é o evento que torna realidade o uso do Balanced Scorecard. A Reunião GEN tem um formato diferente de outras reuniões de gestão usualmente realizadas, pois utiliza a estratégia como centro das discussões e das tomadas de decisão. Ela pressupõe uma intensa preparação composta por levantamento de informações, análise da performance estratégica, definição da pauta e preparação dos líderes.O pós-reunião é composto pelo acompanhamento das ações e decisões definidas durante a reunião.A 3GEN possui uma abordagem prática para definição, implantação e acompanhamento das reuniões GEN.</p>


			<p class="red"><strong>SOLUÇÃO TECNOLÓGICA</strong></p>

