							<h3>Hospital Management Summit</h3>
							
							<p>Gestores estão buscando cada vez mais otimizar os recursos das instituições de saúde em busca de uma administração eficiente, financeiramente sustentável e que atenda a todas as exigências de um mercado a cada dia mais competitivo e profissionalizado. A busca pela modernização das práticas de gestão é um tema presente no cotidiano dos administradores hospitalares.</p>
							
							<p>Em função deste promissor cenário é fundamental que os hospitais estejam preparados com uma equipe gestora qualificada, com sistemas de gestão e administração eficientes e também se atualizem sobre as tendências e oportunidades para competir de forma sustentável e oferecendo excelência nos serviços.</p>
							
							<p>Suprir esta demanda em um evento que ofereça práticas bem sucedidas, soluções de mercado inovadoras e um ambiente ideal para troca de experiências é o objetivo do <strong>HMS</strong>.</p>
							
							<p>Entre os dias 22 e 24 de agosto, o Amcham Business Center será palco do <strong>Fórum de Práticas, Inovação e Negócios em Hospitais</strong>.</p>
							
							<p>Entre as principais patrocinadoras desta Conferência, está a <strong>3GEN Gestão Estratégica</strong>. A 3GEN será representada por seu sócio-diretor, <strong>Roberto Campos</strong>, que presidirá a sessão <em>Planejamento Estratégico e Balanced ScoreCard</em> e ministrará uma palestra sobre “<em>O BSC inserido no desafio da execução da estratégia de hospitais</em>”.</p>
							
							<p>Este importante evento, voltado para o setor de hospitais privados brasileiro, irá reunir mais de 200 profissionais representantes das diferentes áreas que envolvem a administração hospitalar de todo país.</p>
							
