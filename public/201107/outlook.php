							<h3>Panorama e Tendências para o Setor de Saúde no Brasil</h3>
							
							<p><strong>Por Otávio Calixto</strong></p>
							
							<p>Poucos setores da economia brasileira experimentaram e experimentam um contexto tão dinâmico e desafiador quanto o setor de saúde vem enfrentando ao longo desta última década. O surgimento de grandes empresas no setor, a ascensão de novos contingentes populacionais ao nível de consumidores potenciais, regulações mais estritas e constantes movimentos de consolidação ilustram um pouco do que vem sendo o dia-a-dia deste mercado cada vez mais importante na economia brasileira.</p>
							
							<p>Os próximos anos apontam em todos os sentidos para a intensificação destes movimentos e para uma continuidade do crescimento e consolidação do setor, bem como para uma pressão cada vez mais aguda na busca de sinergias e otimização de custos.</p>
							
							<h4>Elos da cadeia e o contexto setorial</h4>
							
							<p>O entendimento clássico sobre o mercado de saúde assinala três grandes entes da cadeia setorial como os determinantes da dinâmica do mercado. Pagadores, Provedores e Pacientes são colocados como os agentes estritos do setor, com naturezas, atribuições e complexidade de atuação bastante específicas.</p>
							
							<p>Os últimos anos revelaram um desafiador contexto de mudanças para alguns integrantes destes elos da cadeia, vindo a transformar todo o panorama do setor de saúde. Compreender as bases destas mudanças recentes é etapa fundamental no entendimento dos movimentos futuros do setor.</p>
							
							<p>Dentro deste contexto dois termos descrevem de forma bastante precisa este conjunto recente de mudanças: “consolidação” e “custo”. Estas duas expressões assumiram um sentido superior as disciplina de gestão que lhes vem embutida, e tornaram-se uma realidade concreta para as organizações do setor.</p>
							
							<h4>Consolidação e pressão por custos: um movimento irremediável</h4>
							
							<p>No que tange a consolidação o cenário está claramente estabelecido, o setor foi às compras nos últimos anos e já assume uma posição de destaque em operações de fusão e aquisição. Em 2010, o mercado de saúde assumiu a quinta posição em transações de compra de empresas no Brasil, com um valor médio de transação de US$ 108,3 milhões. Quando analisados os dois últimos anos, foram um total de 38 transações, com a realização de grandes negócios como as operações de aquisição da Medial Saúde pela Amil no valor de R$ 612,5 milhões e a compra pela Hypermarcas do Laboratório de Genéricos Neo Química por R$ 1,3 bilhões.</p>
							
							<p>Desde 2001 a 2009 o número de operadoras de saúde em todo Brasil caiu de 2.700 para 1.700, demonstrando um expressivo movimento de consolidação do setor. No entanto, tudo indica ainda haver espaço para o aumento destas transações, pois uma vez comparado com o setor de saúde nos Estados Unidos, o mercado nacional ainda apresenta considerável grau de dispersão, no Brasil as 10 maiores operadoras de saúde concentram cerca de 25% do mercado, enquanto nos EUA a concentração do top 10 é de 38%.</p>
							
							<p>A compreensão destes movimentos e de como eles apontam ou dão indícios para o futuro do setor deve basear-se em dois elementos chave das recentes fusões e aquisições: o primeiro deles é a direção destas transações, ou seja, em que segmentos as empresas estão buscando os alvos de compra; e o segundo é o objetivo da aquisição, ou seja, o motivador central do negócio.</p>
							
							<p>Dois casos ilustram bem o tema da direção com que estes negócios ocorrem, o caso Amil e o caso Fleury. A compra ou posse de operações nos diversos segmentos do mercado de saúde (desde o <em>premium</em> até o básico) possibilitou um crescimento de receitas e de base de credenciados em escala quase que geométrica. Ambas as empresas foram protagonistas do setor na última década e seguem uma estratégia de aquisição bem clara: consolidação intra e extra segmentos de negócio.</p>
							
							<p>A consolidação intra e extra segmento de negócio tem sido uma grande fonte de vantagem competitiva para as empresas do setor e deve continuar a ser o principal alavancar e direcionador dos negócios do setor nos próximos anos. Somente o Laboratório Fleury realizou cerca de 23 compras de empresas desde 2002, passando a operar praticamente em todos segmentos, já a Amil ascendeu a posição de um dos principais <em>players</em> do setor, com cerca de 1,4 milhões de vidas e a maior rede credenciada, com mais de 3.000 hospitais e 60.000 clínicas, valores fundamentalmente potencializados pelas recentes aquisições, como Medial e Lincx (anunciada no último dia 25 de maio, no valor total de R$ 170 milhões).</p>
							
							<p>Já o objetivo das operações de aquisição no setor continuará a ser fortemente vinculado ao alcance de sinergias com a fusão de negócios complementares. Basicamente a busca de sinergias em qualquer negócio é focada em 3 possibilidades de ganho: de mercado, de gestão, e de custo; nos quais o setor de saúde apresenta uma especial confluência de possibilidades de ganho.</p>
							
							<p>As duas primeiras sinergias passarão a ocupar o papel de ganhos adicionais das operações de compra, com os ganhos de mercado basicamente focados nas possibilidades de expansão do número de vidas e na ampliação do portfólio de marcas; e os ganhos de gestão vinculados as possibilidades de centralização e fusão de estruturas correlatas entre a empresa adquirente e a adquirida.</p>
							
							<p>Já os ganhos de custo continuarão a ganhar escala significativa no peso das aquisições e irão assumir o papel de principal direcionador da busca de ganhos sinérgicos no setor. O tema da lucratividade assumiu posição de palavra de ordem entre as empresas de saúde, fazendo com que a pressão por custo passasse a fazer cada vez mais parte do dia-a-dia dos negócios neste mercado.</p>
							
							<p>Provedores de saúde em geral, e em especial hospitais e clínicas passarão a sofrer uma pressão ainda maior por redução de custo de procedimentos, pois, além da lucratividade, a consolidação do setor tende a tornar a competição ainda mais aguda. Movimentos de integração vertical, no sentido de aquisições de hospitais por operadoras de saúde, já são uma clara realidade e deverão tornar-se mais intensos no curto e médio prazo, principalmente em grandes centros urbanos onde há demanda que justifique a aquisição de infra-estrutura hospitalar.</p>
							
							<p>Aos hospitais e clínicas caberá cada vez mais a busca de rentabilidade com um posicionamento próximo ao que seria o de um <em>niche player</em> (expressão que designa uma organização que busca atuar em nichos específicos do mercado), buscando estabelecer uma forte posição de excelência em certas especialidades de forma a alcançar uma diferenciação em relação aos demais provedores. Uma comprovação disto é o fato de mais da metade dos 16 hospitais brasileiros certificados pela <em>Joint Comission International (JCI)</em> ser reconhecido por ter altíssimo nível de excelência em apenas 2 a 3 especialidades, com as demais áreas assumindo posições complementares.</p>
							
							<h4>Crescimento da base de clientes e consumidor assumindo papel de destaque</h4>
							
							<p>Se “consolidação” e “custo” são as palavras em voga para os pagadores e provedores no mercado de saúde, “crescimento” certamente é a palavra de destaque para o último eixo desta cadeia: os pacientes.</p>
							
							<p>O contingente populacional que assumiu maior evidência para o mercado de saúde nos últimos foi a nova classe C. Nada menos do que 100 milhões de brasileiros compõem este grupo, com um poder anual de compra de cerca de 430 bilhões de reais, ou, 31% da renda nacional. Outra vertente da população que assumiu posição de evidência para o setor de saúde foram os idosos, atualmente eles perfazem cerca de 20 milhões de vidas, e com o envelhecimento da população a previsão é de que em 2020 este número chegue a 30 milhões de pessoas.</p>
							
							<p>Outro dado que demonstra quanto espaço ainda há para o crescimento deste mercado é o gasto médio do brasileiro com saúde. Segundo dados da OMS, enquanto um americano gasta cerca de US$ 7.000 com saúde ao ano, um brasileiro médio gasta apenas US$ 840, para efeito de comparação um argentino em média gasta US$ 1.300 por ano com saúde. A expansão deste montante de gasto tem seu crescimento tido como certo, pois historicamente o aumento da renda per capta em um país tende a levar a crescimentos exponenciais no montante gasto com saúde.</p>
							
							<p>Quando analisamos a proporção destes gastos com o PIB (total das riquezas geradas no país no período de um ano), a diferença novamente aponta um espaço importante para a expansão do setor. Enquanto nos Estados Unidos os gastos com saúde representam 12% do PIB, no Brasil este percentual são cerca de meros 4%. Nos demais países da América Latina a média é próxima a 5%, sendo que a recomendação da Organização Pan-Americana de Saúde é de que este gasto represente no mínimo 6% do PIB, nos países da OCDE (organização que reúne os principais países desenvolvidos) este valor está entre 6,5% e 15,6%.</p>
							
							<p>Além dos fatores renda e representatividade sobre o PIB, outro elemento traz perspectivas de prosperidade para o mercado de saúde: a penetração dos planos de saúde no Brasil. Em levantamento recente publicado pela revista Exame, os dados apontavam que apenas 22% da população possuem acesso a esse serviço, já nas faixas de renda superiores a cinco salários mínimos a penetração é de 93%. Enquanto isso, pesquisa recente do Datafolha realizada com cerca de 3.200 em todo país apontou que o plano de saúde só perde para a casa própria como principal prioridade da população brasileira.</p>
							
							<p>O acesso as classes emergentes deve continuar a assumir papel de prioridade estratégica por muitas operadoras, como é o caso da maior operadora de planos médicos do país, a Bradesco Saúde. A companhia com cerca de 2,7 milhões de vidas já oferece planos regionais até 30% mais baratos do que os planos nacionais, fazendo com que suas vendas neste segmento crescessem 32% no primeiro trimestre deste ano, comparado com 18% de seu crescimento total.</p>
							
							<p>Porém, da mesma forma que o mercado cresce, também devem crescer nos próximos anos a força e poder de barganha dos usuários dos serviços de saúde, bem como regulações mais pesadas e onerosas por parte da autoridade regulatória do setor.</p>
							
							<p>Há diversos exemplos desta tendência, entre eles está a recente resolução da ANS (Agência Nacional de Saúde Suplementar) que entra em vigor no próximo dia 27 de julho permitindo a transferência em todos os tipos de planos: individual, familiar, coletivo empresarial e por adesão. Outra medida de peso que entrou em vigor no ano passado foi a obrigação de cobertura básica para 70 novos procedimentos e ampliação de limite de consulta em algumas especialidades, adicionando mais pressão sobre os custos das empresas do setor, porém beneficiando cerca de 44 milhões de usuários de planos de saúde.</p>
							
							<p>Adicionalmente, a atenção às necessidades do consumidor deste segmento deve ser fator crucial de vantagem competitiva para as organizações do setor no curto, médio e longo prazo. Pois, ao mesmo tempo em que o setor cresce e assume posição de destaque na economia e nas prioridades de gasto do consumidor final, há ainda muita necessidade de evolução na qualidade do serviço prestado e capacidade de adaptação a novas regulações. Para se ter uma idéia dos desafios que há pela frente, em pesquisa encomendada pelo Conselho Federal de Medicina (CFM) ao Instituto Datafolha quase 60% dos usuários de planos de saúde enfrentou algum problema no serviço oferecido ao longo de 2010; e diversas outras importantes propostas de regulação favoráveis ao consumidor devem ir à consulta pública ou entrar em discussão dentro da agência reguladora.</p>
							
							<p>Por fim, o panorama setorial para o mercado de saúde aponta claramente para um próspero e consistente ciclo de crescimento. No entanto preocupações com a dinâmica do mercado e constante aperfeiçoamento de diretrizes de longo prazo, movimentos estratégicos e qualidade nos serviços oferecidos devem continuar a exigir esforços de gestão das empresas do setor. Se tentássemos em poucas palavras definir o horizonte de longo prazo para a saúde, certamente diríamos que “a oportunidade é grande, mas o desafio é enorme”.</p>
							
