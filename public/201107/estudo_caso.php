							<h3>Gestão de portfólio de projetos em empresa de serviço:<br>estudo de caso da Serasa Experian</h3>
							
							<p><strong>Por Fernanda Guedes</strong></p>
							
							<p>Gerir de forma dinâmica o conjunto de projetos de forma a privilegiar a consecução dos objetivos estratégicos da organização. Foi exatamente com este intuito que a Serasa Experian implementou seu modelo de gestão de portfólio de projetos em 2000.</p>
							
							<p>A empresa, que nasceu em 1968 de uma ação cooperada entre bancos que buscavam informações rápidas e seguras para suportar as decisões sobre crédito, consolidou-se como líder no desenvolvimento e integração de conhecimento, tecnologia e serviços de informação para apoiar a validação de dados, concessão de crédito e marketing direto.</p>
							
							<p>Na esteira da evolução do negócio, as práticas de gestão da empresa progrediram na mesma medida, impulsionadas pela sua busca pela excelência, tornando-a uma importante referência nesse tema para o mercado brasileiro. Entre essas práticas, a gestão de portfólio de projetos pode ser considerada hoje um dos principais alicerces da Serasa Experian para garantir que seu foco esteja direcionado para a execução da estratégia.</p>
							
							<p>Desenvolvida internamente com base nas práticas disseminadas pelo Project Management Institute - PMI e adequada à realidade da empresa, a gestão de portfólio da Serasa Experian está diretamente relacionada à estruturação do seu plano de execução da estratégia, construído a partir da metodologia Balanced Scorecard. Com isso, os projetos – sejam eles estratégicos ou operacionais – devem apresentar aderência a um ou mais objetivos estratégicos para estarem credenciados a fazer parte do portfólio da empresa.</p>
							
							<p>Uma vez selecionados, os projetos são submetidos à priorização, visando maximizar os seus resultados a partir da melhor alocação de recursos (financeiros e humanos, principalmente) entre os projetos do portfólio. Para tanto, os critérios vão desde o nível de prioridade atribuído aos próprios objetivos estratégicos da empresa até o potencial desses projetos em gerar resultados.</p>
							
							<p>No que se refere à gestão do portfólio, mensalmente, o Comitê Diretivo se reúne para analisar o desempenho do portfólio de projetos e direcionar eventuais alterações de rotas, como a alocação de novos recursos em determinados projetos. A análise realizada pelo time executivo leva em consideração tanto a evolução do portfólio em relação ao ciclo de vida de cada projeto, quanto suas entregas e resultados, avaliando se o conjunto das iniciativas está sendo capaz de implementar a estratégia da Serasa Experian em sua plenitude.</p>
							
							<p>Todo esse processo é suportado pelo PMO Corporativo que atua em todas as etapas da implementação da estratégia – desde sua formulação, por meio da elaboração dos Mapas Estratégicos em conjunto com as Unidades de Negócio, até o desdobramento e acompanhamento do portfólio de projetos estratégicos e operacionais –, garantindo assim que os recursos e as ações da empresa estejam alinhados à estratégia definida. Além disso, interage diretamente com os gestores dos projetos estratégicos em desenvolvimento, oferecendo consultoria especializada.</p>
							
							<h4>Quadro-síntese da atuação do PMO Corporativo da Serasa Experian</h4>
							
							<img src="images/content/julho_2011_caso_figura-1.jpg" alt="Quadro-síntese da atuação do PMO Corporativo da Serasa Experian" width="607" height="332" />
							
							<p align="right"><em>Fonte: Serasa Experian</em></p>
							
							<p>Além da disciplina de gestão, o forte patrocínio da alta liderança é o que garante, não só a continuidade da gestão do portfólio, mas também a elevação do seu nível de maturidade. O alto e consistente investimento ao longo dos anos em treinamentos dos atuais e potenciais gestores e equipes de projetos comprova o apoio irrestrito da direção a este processo.</p>
							
							<p>Para a Serasa Experian, no entanto, o principal benefício do patrocínio da alta liderança à gestão de portfólio é a mudança cultural promovida nos diferentes níveis organizacionais.  “Estabelecer processos e implementar tecnologias é relativamente fácil dado as práticas e processos já disseminados no mercado, mas somente conseguimos girar a chave para um novo modelo quando a prática está incutida nos valores da empresa”, afirma Alessandra Rodrigues, Project Management Professional que atua no PMO Corporativo da empresa. “Acredito que este foi, é e continuará sendo um diferencial no modelo de gestão de portfólio de projetos na Serasa Experian.”</p>
							
							<p>Na forte base representada pelo tripé pessoas, processos e ferramentas reside a razão pela qual a Serasa Experian tem apresentado uma consistente evolução na gestão do seu portfólio de projetos, confiando que este processo dará sequência à sua trajetória bem-sucedida.</p>
						
